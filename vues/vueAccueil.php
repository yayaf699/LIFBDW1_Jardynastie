	 <h3 id="center" >L'objectif principal de ce site est de simuler virtuellement un jardin. </br>Pour celà vous pourrez ajouter des variétés de plantes, et par la suite générer aléatoirement des parcelles de jardin.</br>Chaque parcelle générée sera différente.</br>
Vous pourrez également afficher l'ensemble des informations concernant les espèces de plantes que vous désirez.</br>
Ce site fonctionne de manière communautaire, chaque utilisateur peut ajouter des variétés afin de les faire découvrir aux autres.</br>
L'onglet "statistiques" vous permettra de voir de nombreuses informations notamment un classement des 5 plus grosses parcelles générée.</br>Afin de ne pas fausser le classement, nous comptons sur votre confiance pour ne générer qu'un nombre de rangs dont vous êtes réellement détenteur.</h3>
